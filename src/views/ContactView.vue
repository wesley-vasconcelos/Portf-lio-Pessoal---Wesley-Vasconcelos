<script setup lang="ts">
import { ref, reactive } from 'vue'

const form = reactive({
  name: '',
  email: '',
  company: '',
  subject: '',
  message: ''
})

const isSubmitting = ref(false)
const submitMessage = ref('')
const submitStatus = ref('')

const submitForm = () => {
  if (isSubmitting.value) return

  isSubmitting.value = true
  submitMessage.value = ''

  if (!form.name.trim() || !form.email.trim() || !form.subject || !form.message.trim()) {
    submitMessage.value = 'Por favor, preencha todos os campos obrigat√≥rios.'
    submitStatus.value = 'error'
    isSubmitting.value = false

    setTimeout(() => {
      submitMessage.value = ''
      submitStatus.value = ''
    }, 3000)
    return
  }

  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/
  if (!emailRegex.test(form.email.trim())) {
    submitMessage.value = 'Por favor, insira um email v√°lido.'
    submitStatus.value = 'error'
    isSubmitting.value = false

    setTimeout(() => {
      submitMessage.value = ''
      submitStatus.value = ''
    }, 3000)
    return
  }

  const name = form.name.trim()
  const email = form.email.trim()
  const company = form.company.trim()
  const subject = getSubjectText(form.subject)
  const message = form.message.trim()

  const whatsappMessage = `Nova mensagem do portf√≥lio!

Nome: ${name}
Email: ${email}
${company ? `Empresa: ${company}` : ''}
Assunto: ${subject}

Mensagem:
${message}

---
Enviado atrav√©s do formul√°rio de contato do portf√≥lio`

  const whatsappNumber = '5511985912734'

  try {
    const encodedMessage = encodeURIComponent(whatsappMessage)
    const whatsappUrl = `https://wa.me/${whatsappNumber}?text=${encodedMessage}`

    submitMessage.value = '‚úÖ Redirecionando para o WhatsApp... Sua mensagem foi preparada!'
    submitStatus.value = 'success'

    form.name = ''
    form.email = ''
    form.company = ''
    form.subject = ''
    form.message = ''

    setTimeout(() => {
      try {
        const newWindow = window.open(whatsappUrl, '_blank')
        if (!newWindow || newWindow.closed || typeof newWindow.closed == 'undefined') {
          window.location.href = whatsappUrl
        }
      } catch (e) {
        window.location.href = whatsappUrl
      }
    }, 1000)

  } catch (error) {
    submitMessage.value = 'Erro ao processar mensagem. Tente novamente.'
    submitStatus.value = 'error'
  }

  isSubmitting.value = false

  setTimeout(() => {
    submitMessage.value = ''
    submitStatus.value = ''
  }, 5000)
}

const handleButtonClick = () => {
  submitForm()
}



const getSubjectText = (subject: string): string => {
  const subjects: Record<string, string> = {
    'job': 'Oportunidade de Trabalho',
    'project': 'Projeto Freelance',
    'collaboration': 'Colabora√ß√£o',
    'consultation': 'Consultoria',
    'other': 'Outro'
  }
  return subjects[subject] || subject
}
